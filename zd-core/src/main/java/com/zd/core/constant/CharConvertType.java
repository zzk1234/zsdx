package com.zd.core.constant;

/**
 * Created by luoyibo on 2017-06-05.
 * 将中文转换成拼音后的返回正理常量
 */
public class CharConvertType {
    /**
     * 全部字符大写
     */
    public final static String UPPERCASE = "UPPERCASE";

    /**
     * 全部字符小写
     */
    public final static String LOWERCASE = "LOWERCASE";

    /**
     * 首字母大写
     */
    public final static String FIRSTUPPER = "FIRSTUPPER";

    /**
     * 转换后仅返回每个单词的首字母
     */
    public  final  static  String RETURNFIRST = "RETURNFIRST";

    /**
     * 转换后返回每个单词的全部字母
     */
    public final static String RETURNALL = "REUTRNALL";
}
